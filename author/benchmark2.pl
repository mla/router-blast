#!/usr/bin/env perl

# Usage: $0 [num-routes]

# Adds routes of varying lengths with and without placeholders
# to give an idea of how performance scales.

use strict;
use warnings;
use lib 'lib', '../lib';
use Benchmark qw/ :all /;
use Router::Boom;
use Router::Boom::Method;
use Router::Right;
use Router::Simple;

my $num_routes  = shift || 5_000;

{
  my @words =
    map { lc $_ }
    grep { length }
    map { s/^\s+|\s+$//g; $_ }
    grep { defined } map { chomp; $_ }
    <DATA>
  ;

  sub word {
    my $word = shift @words;
    push @words, $word;
    return $word;
  }
}

$| = 1;
srand(1);

my %router = (
  router_right       => Router::Right->new,
  router_simple      => Router::Simple->new,
  router_boom        => Router::Boom->new,
  router_boom_method => Router::Boom::Method->new,
);

my $payload = {};
# parameters: name, route_path
my %add = (
  router_right => sub {
    $router{router_right}->add($_[0] => $_[1], $payload);
  },

  router_simple => sub {
    $router{router_simple}->connect($_[1], $payload);
  },

  router_boom   => sub {
    $router{router_boom}->add($_[1], $payload);
  },

  router_boom_method => sub {
    $router{router_boom_method}->add(undef, $_[1], $payload);
  },
);


print STDERR "Preparing routes... ";
my @paths;
while (@paths < $num_routes) {
  my $num_words = rand(3) + 1;
  my $path = join '/', '', map { word() } (1..$num_words);

  my $idx = @paths + 1;
  push @paths, $path;
  $_->("word$idx", $path) foreach values %add;
  # print "Added route: $path\n";

  $idx++;
  my $path_with_id = "$path/{id}";
  push @paths, $path_with_id;
  $_->("word${idx}_id", $path_with_id) foreach values %add;
  # print "Added route: $path_with_id\n";
}
print STDERR scalar(@paths), " added.\nStarting test\n";


my $seed = 10;
my %seeded;

cmpthese(
  -1,
  {
    'Router::Boom' => sub {
      srand($seed) unless $seeded{router_boom}++;
  
      my $path = $paths[ rand(@paths) ];
      #warn "R:B $path\n";
      $router{router_boom}->match($path)
        or die "Router::Boom match failed";
    },

    'Router::Boom::Method' => sub {
      srand($seed) unless $seeded{router_boom_method}++;

      my $path = $paths[ rand(@paths) ];
      #warn "R:B:M $path\n";
      $router{router_boom_method}->match('GET', $path)
        or die "Router::Boom::Method match failed";
    },

    'Router::Simple' => sub {
      srand($seed) unless $seeded{router_simple}++;

      my $path = $paths[ rand(@paths) ];
      #warn "R:S $path\n";
      $router{router_simple}->match($path)
        or die "Router::Simple match failed";
    },

    'Router::Right' => sub {
      srand($seed) unless $seeded{router_right}++;

      my $path = $paths[ rand(@paths) ];
      #warn "R:R $path\n";
      $router{router_right}->match($path)
        or die "Router::Right match failed";
    },
  },
);

1;

